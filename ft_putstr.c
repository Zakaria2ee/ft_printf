#include "ft_printf.h"

void	ft_putstr(char *s, int *count)
{
	int	i;

	i = 0;
	if (s != '\0')
	{
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			(*count)++;
			i++;
		}
	}
}
